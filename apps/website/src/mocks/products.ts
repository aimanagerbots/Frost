export function getAllProducts(): WebsiteProduct[] {
  return allProducts;
}

export function getProductsByCategory(category: string): WebsiteProduct[] {
  return allProducts.filter((p) => p.category === category);
}

export function getProductBySlug(slug: string): WebsiteProduct | undefined {
  return allProducts.find((p) => p.slug === slug);
}

export function getFeaturedProducts(): WebsiteProduct[] {
  return allProducts.filter((p) => p.isBestSeller || p.isNew);
}

export function getProductsByStrain(strainName: string): WebsiteProduct[] {
  return allProducts.filter(
    (p) =>
      p.strainName !== undefined &&
      p.strainName.toLowerCase() === strainName.toLowerCase(),
  );
}
