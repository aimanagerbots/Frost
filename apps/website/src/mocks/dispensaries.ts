export function getAllDispensaries(): DispensaryLocation[] {
  return dispensaries;
}

export function getDispensariesByRegion(region: string): DispensaryLocation[] {
  return dispensaries.filter(
    (d) => d.region.toLowerCase() === region.toLowerCase()
  );
}

export function getDispensaryBySlug(slug: string): DispensaryLocation | undefined {
  return dispensaries.find((d) => d.slug === slug);
}

export function getDispensaryById(id: string): DispensaryLocation | undefined {
  return dispensaries.find((d) => d.id === id);
}

export function getAllDispensarySlugs(): string[] {
  return dispensaries.map((d) => d.slug);
}
