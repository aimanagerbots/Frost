export function getTasks(filters?: TaskFilter): Promise<Task[]> {
  return new Promise((resolve) => {
    setTimeout(() => {
      let result = [...tasks];

      if (filters?.status) {
        result = result.filter((t) => t.status === filters.status);
      }
      if (filters?.priority) {
        result = result.filter((t) => t.priority === filters.priority);
      }
      if (filters?.assignee) {
        result = result.filter((t) => t.assignee === filters.assignee);
      }
      if (filters?.module) {
        result = result.filter((t) => t.module === filters.module);
      }
      if (filters?.source) {
        result = result.filter((t) => t.source === filters.source);
      }
      if (filters?.pipelineCode) {
        const code = filters.pipelineCode;
        result = result.filter((t) => {
          if (!t.pipelineCode) return false;
          // Support prefix matching (A, I, R) and exact match (A3, I1, etc.)
          return code.length === 1
            ? t.pipelineCode.startsWith(code)
            : t.pipelineCode === code;
        });
      }
      if (filters?.search) {
        const q = filters.search.toLowerCase();
        result = result.filter(
          (t) =>
            t.title.toLowerCase().includes(q) ||
            t.description.toLowerCase().includes(q)
        );
      }

      resolve(result);
    }, 300);
  });
}

