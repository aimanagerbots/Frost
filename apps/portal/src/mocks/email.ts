export async function getEmails(folder?: EmailFolder): Promise<Email[]> {
  await new Promise((r) => setTimeout(r, 300));
  if (folder) return EMAILS.filter((e) => e.folder === folder);
  return EMAILS;
}

export async function getEmailMetrics(): Promise<EmailMetrics> {
  await new Promise((r) => setTimeout(r, 200));
  const inbox = EMAILS.filter((e) => e.folder === 'inbox');
  return {
    unread: inbox.filter((e) => !e.read).length,
    todayReceived: inbox.filter((e) => e.date.startsWith('2026-03-07')).length,
    needsResponse: inbox.filter((e) => !e.read && e.crmLinked).length,
    avgResponseTime: '2.4 hrs',
  };
}
