// ─── Organic Trend (30 days) ──────────────────────────────────

function generateOrganicTrend(): OrganicTrendPoint[] {
  const points: OrganicTrendPoint[] = [];
  const baseVisits = 240;
  for (let i = 29; i >= 0; i--) {
    const d = new Date();
    d.setDate(d.getDate() - i);
    const dayOfWeek = d.getDay();
    const weekendDip = dayOfWeek === 0 || dayOfWeek === 6 ? 0.7 : 1;
    const trendGrowth = 1 + (29 - i) * 0.008;
    const noise = 0.85 + Math.random() * 0.3;
    points.push({
      date: d.toISOString().slice(0, 10),
      visits: Math.round(baseVisits * weekendDip * trendGrowth * noise),
    });
  }
  return points;
}

