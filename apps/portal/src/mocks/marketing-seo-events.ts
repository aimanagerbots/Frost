export async function getSEOMetrics(): Promise<SEOMetrics> {
  await delay(300);
  const published = blogPosts.filter((p) => p.status === 'published');
  const totalViews = published.reduce((sum, p) => sum + (p.views ?? 0), 0);
  const avgScore = Math.round(published.reduce((sum, p) => sum + p.seoScore, 0) / published.length);
  const ranked = seoKeywords.filter((k) => k.currentRank && k.currentRank <= 10);
  const allRanked = seoKeywords.filter((k) => k.currentRank);
  const avgPos = allRanked.length > 0
    ? Math.round(allRanked.reduce((sum, k) => sum + (k.currentRank ?? 0), 0) / allRanked.length * 10) / 10
    : 0;
  const topKw = seoKeywords.filter((k) => k.currentRank).sort((a, b) => (a.currentRank ?? 999) - (b.currentRank ?? 999))[0];
  return {
    publishedPosts: published.length,
    totalViews,
    avgSeoScore: avgScore,
    organicTraffic: Math.round(totalViews * 0.72),
    totalOrganicVisits: 8400,
    top10Count: ranked.length,
    avgPosition: avgPos || 14.2,
    topKeyword: topKw?.keyword ?? '',
    topKeywordRank: topKw?.currentRank ?? 0,
  };
}

export async function getEventMetrics(): Promise<EventMetrics> {
  await delay(300);
  const completed = events.filter((e) => e.status === 'completed');
  const upcoming = events.filter((e) => e.status === 'planned' || e.status === 'confirmed');
  const withROI = completed.filter((e) => e.roiMeasured);
  const avgROI = withROI.length > 0
    ? Math.round(withROI.reduce((sum, e) => sum + (e.roiMeasured?.lift ?? 0), 0) / withROI.length * 10) / 10
    : 0;
  const totalBudget = events.filter((e) => e.status !== 'cancelled').reduce((sum, e) => sum + e.budget, 0);
  const vendorDays = events.filter((e) => e.type === 'vendor-day' && (e.status === 'confirmed' || e.status === 'planned'));
  const now = new Date();
  const thisMonth = vendorDays.filter((e) => {
    const d = new Date(e.date);
    return d.getMonth() === now.getMonth() && d.getFullYear() === now.getFullYear();
  });
  const nextTradeShow = events
    .filter((e) => e.type === 'trade-show' && new Date(e.date) > now)
    .sort((a, b) => new Date(a.date).getTime() - new Date(b.date).getTime())[0];
  const daysToTradeShow = nextTradeShow
    ? Math.ceil((new Date(nextTradeShow.date).getTime() - now.getTime()) / (1000 * 60 * 60 * 24))
    : 0;
  return {
    upcoming: upcoming.length,
    completedThisQuarter: completed.length,
    totalBudget,
    avgROI,
    vendorDaysThisMonth: thisMonth.length,
    avgRevenueLift: 32,
    topLocation: 'Greenfield Dispensary',
    nextTradeShowDays: daysToTradeShow,
  };
}

export async function getAdMetrics(): Promise<AdMetrics> {
  await delay(300);
  const active = adCampaigns.filter((c) => c.status === 'active');
  const allSpent = active.reduce((sum, c) => sum + c.spent, 0) + adCampaigns.filter((c) => c.status === 'paused').reduce((sum, c) => sum + c.spent, 0);
  const allBudget = active.reduce((sum, c) => sum + c.budget, 0) + adCampaigns.filter((c) => c.status === 'paused').reduce((sum, c) => sum + c.budget, 0);
  const mtdImpressions = active.reduce((sum, c) => sum + c.impressions, 0) + adCampaigns.filter((c) => c.status === 'paused').reduce((sum, c) => sum + c.impressions, 0);
  const mtdClicks = active.reduce((sum, c) => sum + c.clicks, 0) + adCampaigns.filter((c) => c.status === 'paused').reduce((sum, c) => sum + c.clicks, 0);
  const mtdConversions = active.reduce((sum, c) => sum + c.conversions, 0) + adCampaigns.filter((c) => c.status === 'paused').reduce((sum, c) => sum + c.conversions, 0);
  const flagged = adCampaigns.filter((c) => c.complianceStatus === 'flagged');
  return {
    activeCampaigns: active.length,
    totalSpendMTD: allSpent,
    totalBudget: allBudget,
    totalImpressions: mtdImpressions,
    totalClicks: mtdClicks,
    avgCPC: mtdClicks > 0 ? Math.round((allSpent / mtdClicks) * 100) / 100 : 0,
    totalConversions: mtdConversions,
    flaggedCampaigns: flagged.length,
  };
}
