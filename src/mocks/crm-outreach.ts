function makeRecipients(
  accountPairs: [string, string][],
  statuses: ('sent' | 'opened' | 'responded' | 'ordered')[],
): CampaignDetail['recipients'] {
  return accountPairs.map(([accountId, accountName], i) => {
    const status = statuses[i % statuses.length];
    const sentAt = '2026-03-05T06:00:00Z';
    return {
      accountId,
      accountName,
      status,
      sentAt,
      openedAt: status !== 'sent' ? '2026-03-05T07:12:00Z' : null,
      respondedAt: status === 'responded' || status === 'ordered' ? '2026-03-05T08:45:00Z' : null,
      orderPlaced: status === 'ordered',
    };
  });
}

